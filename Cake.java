class Cake {
  String shape;
  String flavour;
  double price;
  String design;
  double weight;
  String colour;

  public Cake(String shape, String flavour, double price, String design, double weight, String colour) {
    this.shape = shape;
    this.flavour = flavour;
    this.price = price;
    this.design = design;
    this.weight = weight;
    this.colour = colour;
  }
}