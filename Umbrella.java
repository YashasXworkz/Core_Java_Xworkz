class Umbrella {
  String brandName;
  String color;
  String handleType;
  double price;
  double weight;

  public Umbrella(String brandName, String color, String handleType, double price, double weight) {
    this.brandName = brandName;
    this.color = color;
    this.handleType = handleType;
    this.price = price;
    this.weight = weight;
  }
}